package com.infrasofttech.microfinance.entityBeans.master;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

import lombok.Data;

@Entity
@Table(name = "ChartTypesMaster")
@Data
public class ChartTypesMasterEntity {

	 @Id	
	 @Column(name = "mcharttypes",  columnDefinition = "NVarChar(5)")
	 String mcharttypes;	 
	 	
	 @Column(name = "mchartids",  columnDefinition = "NVarChar(400) default ''")
	 String mchartids;	 
	 	
	 @Column(name = "mimageurl",columnDefinition = "NVarChar(400) default ''")
	 String mimageurl;
	
}
